  // Check if we should start polling based on initial context
  if (props.sessionId && (
      props.context?.explanation_metadata?.generated_by?.includes("AI") ||
      props.context?.explanation_metadata?.extraction_method === "ai_generation"
  )) {
    // This might be an AI-generated explanation that was enhanced
    const currentSessionId = props.sessionId
    if (currentSessionId) {
      startAIPolling(currentSessionId)
    }
  }
})

onUnmounted(() => {
  stopAIPolling()
})
</script>

