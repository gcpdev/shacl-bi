const getConstraintSubtitle = () => {
  // Provide specific subtitle based on constraint context
  if (props.context?.requiredClass) {
    return `Not an instance of: ${formatPropertyName(props.context.requiredClass)}`
  }
  if (props.context?.requiredDatatype) {
    return `Required type: ${props.context.requiredDatatype}`
  }
  if (props.context?.minValue !== undefined) {
    return `Minimum value: ${props.context.minValue}`
  }
  if (props.context?.maxValue !== undefined) {
    return `Maximum value: ${props.context.maxValue}`
  }
  if (props.context?.minLength !== undefined) {
    return `Minimum length: ${props.context.minLength} characters`
  }
  if (props.context?.maxLength !== undefined) {
    return `Maximum length: ${props.context.maxLength} characters`
  }
  if (props.context?.pattern) {
    return `Pattern: ${props.context.pattern}`
  }
  return null
}

const getStatusClass = () => {
  if (props.hasError) return 'status-error'
  if (props.isLoading) return 'status-loading'
  if (props.canApplyFix) return 'status-ready'
  return 'status-incomplete'
}

const getStatusText = () => {
  if (props.hasError) return 'Error'
  if (props.isLoading) return 'Processing'
  if (props.canApplyFix) return 'Ready'
  return 'Incomplete'
}

const formatPropertyName = (uri) => {
  if (!uri) return ''
  if (uri.includes(':') && !uri.startsWith('http')) {
    return uri
  }
  const parts = uri.split(/[#\/]/)
  return parts[parts.length - 1] || uri
}

const formatValue = (value) => {
  if (value === null || value === undefined) return 'null'
  if (typeof value === 'string') {
    // Check if it's a URI
    if (value.startsWith('http')) {
      return formatPropertyName(value)
    }
    return `"${value}"`
  }
  return String(value)
}

const formatConstraints = (constraints) => {
  if (!constraints || !Array.isArray(constraints)) return 'None'
  return constraints.map(c => formatPropertyName(c)).join(', ')
}

const formatTimestamp = (timestamp) => {
  if (!timestamp) return 'Unknown'
  try {
    const date = new Date(timestamp)
    return date.toLocaleDateString() + ' at ' + date.toLocaleTimeString()
  } catch (e) {
    return timestamp // Return original if parsing fails
  }
}

const rejectFix = () => {
  emit('reject-fix', {
    focusNode: props.focusNode,
    resultPath: props.resultPath,
    value: props.value,
    constraintComponent: props.constraintComponent
  })
}

const applyFix = () => {
  emit('apply-fix', {
    focusNode: props.focusNode,
    resultPath: props.resultPath,
    value: props.value,
    constraintComponent: props.constraintComponent,
    context: props.context
  })
}
// AI Processing Polling Methods
const startAIPolling = (currentSessionId) => {
  if (!currentSessionId) return
  
  sessionId.value = currentSessionId
  isProcessingAI.value = true
  
  // Clear any existing polling
  stopAIPolling()
  
  // Start polling every 3 seconds
  aiProcessingPollingInterval.value = setInterval(async () => {
    try {
      const response = await fetch(`/api/background-status/${sessionId.value}`)
      const data = await response.json()
      
      if (data.status === "completed") {
        // AI processing completed, refresh explanation data
        stopAIPolling()
        await refreshExplanation()
      } else if (data.status === "failed") {
        // AI processing failed
        stopAIPolling()
      }
    } catch (error) {
      console.error("Error checking AI processing status:", error)
    }
  }, 3000)
}

const stopAIPolling = () => {
  if (aiProcessingPollingInterval.value) {
    clearInterval(aiProcessingPollingInterval.value)
    aiProcessingPollingInterval.value = null
  }
  isProcessingAI.value = false
}

const refreshExplanation = async () => {
  try {
    if (!props.focusNode) return
    
    const session_id = localStorage.getItem("shacl_session_id")
    const response = await api.post("/api/explanation", {
      violation: {
        focus_node: props.focusNode,
        property_path: props.resultPath,
        constraint_id: props.constraintComponent,
        value: props.value,
        message: props.message
      },
      session_id
    })
    
    // Update context with new explanation data
    if (response.data) {
      // Emit event to parent to update context
      emit("explanation-updated", {
        focusNode: props.focusNode,
        resultPath: props.resultPath,
        constraintComponent: props.constraintComponent,
        explanationData: response.data
      })
    }
  } catch (error) {
    console.error("Error refreshing explanation:", error)
  }
}

